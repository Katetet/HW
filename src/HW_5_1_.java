public class HW_5_1_ {

    public int getSum(int a, int b) {
        return a + b;
    }

    public int getSub(int a, int b) {
        return a - b;
    }

    public double getDiv(double a, int b) {
        return a / b;
    }

    public int getMulti(int a, int b) {
        return a * b;
    }

}
